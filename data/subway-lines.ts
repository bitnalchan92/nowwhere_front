// 역명으로 역 정보 찾기
export const findStationByName = (name: string): { line: SubwayLine; station: SubwayStation } | null => {
  for (const line of subwayLines) {
    const station = line.stations.find(s => s.name === name)
    if (station) {
      return { line, station }
    }
  }
  return null
}

// 특정 호선의 역 목록 가져오기
export const getStationsByLine = (lineName: string): SubwayStation[] => {
  const line = subwayLines.find(l => l.name === lineName)
  return line?.stations ?? []
}
